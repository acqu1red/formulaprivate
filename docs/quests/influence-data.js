  getRandomEvent: () => {
    const availableEvents = RANDOM_EVENTS.filter(event => Math.random() < event.probability);
    if (availableEvents.length === 0) return null;
    
    return availableEvents[Math.floor(Math.random() * availableEvents.length)];
  },
  
