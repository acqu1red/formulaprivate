// Функции для работы с данными
export function getTempleById(id) {
    return templeData[id] || null;
}

export function getAllTemples() {
    return Object.values(templeData);
}

export function getTempleBooks(templeId) {
    const temple = getTempleById(templeId);
    return temple ? temple.books : [];
}

export function getBookById(bookId) {
    for (const temple of getAllTemples()) {
        const book = temple.books.find(b => b.id === bookId);
        if (book) return book;
    }
    return null;
}

export function searchBooks(query) {
    const results = [];
    const lowerQuery = query.toLowerCase();
    
    for (const temple of getAllTemples()) {
        for (const book of temple.books) {
            if (book.title.toLowerCase().includes(lowerQuery) ||
                book.description.toLowerCase().includes(lowerQuery) ||
                book.tags.some(tag => tag.toLowerCase().includes(lowerQuery))) {
                results.push({
                    ...book,
                    templeId: temple.id,
                    templeName: temple.name
                });
            }
        }
    }
    
    return results;
}

export function getBooksByCategory(category) {
    const results = [];
    
    for (const temple of getAllTemples()) {
        for (const book of temple.books) {
            if (book.category === category) {
                results.push({
                    ...book,
                    templeId: temple.id,
                    templeName: temple.name
                });
            }
        }
    }
    
    return results;
}

export function getBooksByDifficulty(difficulty) {
    const results = [];
    
    for (const temple of getAllTemples()) {
        for (const book of temple.books) {
            if (book.difficulty === difficulty) {
                results.push({
                    ...book,
                    templeId: temple.id,
                    templeName: temple.name
                });
            }
        }
    }
    
    return results;
}
